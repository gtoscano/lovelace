// Java program to illustrate the 
// Java.lang.Integer.sum() method 
import java.lang.*; 

public class suma { 

	public static void main(String[] args) 
	{ 
		int a = 62; 
		int b = 18; 
		// It will return the sum of two arguments. 
		System.out.println("The sum is =" + Integer.sum(a, b)); 
	} 
} 



